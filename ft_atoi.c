#include "libft.h"

int	ft_atoi(const char *str)
{
	char	*p;
	int		i;
	int		pre;
	int		ans;

	i = 0;
	p = (char *)str;
	pre = 1;
	ans = 0;
	while (p[i] == 32 || (p[i] >= 9 && p[i] <= 13))
		i++;
	if (p[i] == '-' || p[i] == '+')
	{
		if (p[i] == '-')
			pre = -1;
		i++;
	}
	while (p[i] >= '0' && p[i] <= '9')
	{
		ans = ans * 10 + (p[i] - '0');
		i++;
	}
	return (pre * ans);
}
